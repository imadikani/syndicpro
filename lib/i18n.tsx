// ─── CONTEXT ──────────────────────────────────────────────────────────────────

type Translations = typeof fr;

interface LangCtx {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: keyof Translations) => string;
  months: string[];
}

const LanguageContext = createContext<LangCtx>({
  lang: 'fr',
  setLang: () => {},
  t: (k) => String(fr[k]),
  months: fr.months,
});

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>('fr');

  useEffect(() => {
    const stored = localStorage.getItem('lang') as Lang;
    if (stored === 'fr' || stored === 'en') setLangState(stored);
  }, []);

  function setLang(l: Lang) {
    setLangState(l);
    localStorage.setItem('lang', l);
  }

  const dict = lang === 'en' ? en : fr;

  function t(key: keyof Translations): string {
    const val = dict[key];
    return Array.isArray(val) ? String(val) : String(val);
  }

  return (
    <LanguageContext.Provider value={{ lang, setLang, t, months: dict.months }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  return useContext(LanguageContext);
}

// ─── TOGGLE COMPONENT ─────────────────────────────────────────────────────────

export function LangToggle({ style }: { style?: React.CSSProperties }) {
  const { lang, setLang } = useLanguage();
  return (
    <div style={{ display: 'flex', gap: 0, borderRadius: 100, overflow: 'hidden', border: '1px solid rgba(123,94,167,0.25)', ...style }}>
      {(['fr', 'en'] as Lang[]).map(l => (
        <button
          key={l}
          onClick={() => setLang(l)}
          style={{
            padding: '4px 10px',
            fontSize: 11,
            fontWeight: 600,
            letterSpacing: 0.5,
            textTransform: 'uppercase',
            border: 'none',
            cursor: 'pointer',
            fontFamily: "'DM Sans', sans-serif",
            background: lang === l ? '#7b5ea7' : 'transparent',
            color: lang === l ? 'white' : 'rgba(123,94,167,0.7)',
            transition: 'all 0.15s',
          }}
        >
          {l}
        </button>
      ))}
    </div>
  );
}
